module Main where

import System.Environment

import Cpu
import Executor

main :: IO ()
main = do
  let (_, (_, cpu)) = loadAndExecute prog 0x0600
  putStrLn (show cpu)


prog =
  [ (0x0600, 0xa9), (0x0601, 0x01)
  , (0x0602, 0x8d), (0x0603, 0x00), (0x0604, 0x02)
  , (0x0605, 0xa9), (0x0606, 0x05)
  , (0x0607, 0x8d), ( 0x0608, 0x01), (0x0609, 0x02)
  , (0x060A, 0xa9), (0x060B, 0x08)
  , (0x060C, 0x8d), (0x60D, 0x02), (0x060E,  0x02)
  ]
